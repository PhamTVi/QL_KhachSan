    private void pnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pnLuuActionPerformed
        // TODO add your handling code here:
        if (KiemtraMaSoTonTai(txtMaDV.getText())) {
            int tl = JOptionPane.showConfirmDialog(null,
                    "Ma (" + txtMaDV.getText() + ") Da ton tai, ban co muon cap nhap",
                    "trung ma so", JOptionPane.YES_NO_OPTION);
            if (tl == JOptionPane.NO_OPTION) {
                return;
            } else {
                try {
                    String sqlUpdate = "Update DichVu set TenDV = N'" + txtTenDV.getText() + "'" + ",GiaTien = '" + txtGiaTien.getText() + "' where MaDV = '" + txtMaDV.getText() + "'";
                    int x = ks.statement.executeUpdate(sqlUpdate);
                    if (x >= 1) {
                        HienThiCSDL();
                    }
                    try {

                    } catch (Exception ex) {

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(DichVu.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }  else {
                try {
                    String sqlInsert = "Insert into DichVu(MaDV, TenDV, GiaTien)"
                            + "Values('" + txtMaDV.getText() + "', N'" + txtTenDV.getText() + "', '" + txtGiaTien.getText() + "')";
                    int x = ks.statement.executeUpdate(sqlInsert);
                    if (x >= 1) {
                        HienThiCSDL();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(DichVu.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

    }//GEN-LAST:event_pnLuuActionPerformed

