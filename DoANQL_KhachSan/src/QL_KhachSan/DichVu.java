    public boolean KiemtraMaSoTonTai(String ma) {
        try {
            String ql = "select * from DichVu where MaDV ='" + ma + "'";
            ResultSet rs = ks.statement.executeQuery(ql);
            if (rs.next()) {
                return true;
            } else {
                return false;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return false;
    }

    private void HienThiCSDL() {
        String sql = ("select *from DichVu");
        try {
            ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql);
            dfQLKH.setRowCount(0);
            while (ks.resultset.next()) {
                String ma = ks.resultset.getString("MaDV");
                String ten = ks.resultset.getString("TenDV");
                Double giaTien = ks.resultset.getDouble("GiaTien");
                Vector<Object> vec = new Vector<Object>();
                vec.add(ma);
                vec.add(ten);
                vec.add(giaTien);
                dfQLKH.addRow(vec);
            }

        } catch (SQLException ex) {
            Logger.getLogger(QLKhachSan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

