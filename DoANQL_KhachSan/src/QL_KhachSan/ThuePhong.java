    public boolean KiemtraMaSoTonTai(String ma) {
        try {
            String ql = "select * from ThuePhong where MaThue ='" + ma + "'";
            ResultSet rs = ks.statement.executeQuery(ql);
            if (rs.next()) {
                return true;
            } else {
                return false;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return false;
    }
 private  void HienThiCSDL(){
         String sql = ("select *from KHACHHANG");
        try {
            ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql);
            dfQLTT.setRowCount(0);
            while (ks.resultset.next()) {
                String ma = ks.resultset.getString("MaKH");
                String ten = ks.resultset.getString("TenKH");
                Date ngaySinh = ks.resultset.getDate("NgaySinh");
                String diaC = ks.resultset.getString("DiaChi");
                String sdt = ks.resultset.getString("SDT");
               Vector<Object>  vec = new Vector<Object>();
               vec.add(ma);
               vec.add(ten);
               vec.add(ngaySinh);
               vec.add(diaC);
               vec.add(sdt);
               dfQLTT.addRow(vec);
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(ThuePhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
