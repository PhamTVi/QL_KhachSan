    private void pnThueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pnThueActionPerformed
        // TODO add your handling code here:
         if (txtDatCoc.getText().isEmpty() || txtMaKH.getText().isEmpty() || txtMaP.getText().isEmpty() || txtDatCoc.getText().isEmpty() || txtNgayRA.getText().isEmpty() || txtMaThue.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập ngày vào!");
        } else {
           
            if (KiemtraMaSoTonTai(txtMaThue.getText())) {
                JOptionPane.showMessageDialog(null, "Đã trung Mã phòng");
            } else {
                try {
                    String sqlInsert = "Insert into THUEPHONG "
                            + "Values('" +txtMaThue.getText()+"', '" + txtMaP.getText() + "', '" + txtNgayVao.getText() + "' , '" + txtMaKH.getText() + "'  , '" + txtMaNV.getText() + "' , '" + txtDatCoc.getText() + "' , '" + txtNgayRA.getText() + "')";
                    int x = ks.statement.executeUpdate(sqlInsert);
                    if (x >= 1) {
                        JOptionPane.showMessageDialog(null, "Đã Thuê Phòng");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(ThuePhong.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_pnThueActionPerformed

