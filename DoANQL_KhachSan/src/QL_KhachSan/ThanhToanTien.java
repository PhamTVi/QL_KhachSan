    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(txtNgayIn.getText().isEmpty()){
              JOptionPane.showMessageDialog(null, "Vui lòng nhập Ngày vào");
        }
        else{
        String sql = ("exec Xem_ThuePhong_VaoNgay '"+txtNgayIn.getText()+"'");
        try {
           ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql);
            dfThue.setRowCount(0);
            while (ks.resultset.next()) {
                String Mathue = ks.resultset.getString("MaThue");
                String MaP = ks.resultset.getString("MaP");
                Date NgayVao = ks.resultset.getDate("NgayVao");
                String MaKH = ks.resultset.getString("MaKH");
                String MaNV = ks.resultset.getString("MaNV");
                Double DatCoc = ks.resultset.getDouble("DatCoc");
                Date NgayRa = ks.resultset.getDate("NgayRa");
                Vector<Object> vec = new Vector<Object>();
                vec.add(Mathue);
                vec.add(MaP);
                vec.add(NgayVao);
                vec.add(MaKH);
                vec.add(MaNV);
                vec.add(DatCoc);
                vec.add(NgayRa);
                dfThue.addRow(vec);
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(QLKhachSan.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
         String sql = ("exec Xem_DichVu '"+txtNgayIn.getText()+"'");
          try {
            ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql);
            dfSDDV.setRowCount(0);
            while (ks.resultset.next()) {
                String maSD = ks.resultset.getString("MaSD");
                String maThue = ks.resultset.getString("MaThue");
                String maDV = ks.resultset.getString("MaDV");
                Date ngaySD = ks.resultset.getDate("NgaySD");
                Double giaTien = ks.resultset.getDouble("DonGia");

                Vector<Object> vec = new Vector<Object>();
                vec.add(maSD);
                vec.add(maThue);
                vec.add(maDV);
                vec.add(ngaySD);
                vec.add(giaTien);
                dfSDDV.addRow(vec);
            }

        } catch (SQLException ex) {
            Logger.getLogger(SDDichVU.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

