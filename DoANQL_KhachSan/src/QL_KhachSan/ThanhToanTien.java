    private void JTableThueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTableThueMouseClicked
        // TODO add your handling code here:
         int row = this.JTableThue.getSelectedRow();
        String sql = ("exec Xuat_TenKhachHang '"+(String)JTableThue.getValueAt(row, 0)+ "'");
          try {
            ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql);
            dfSDDV.setRowCount(0);
            while (ks.resultset.next()) {
                txtHoTenKH.setText(ks.resultset.getString("TenKH"));
                txtHoTenNV.setText(ks.resultset.getString("HoTenNV"));     
                
            }

        } catch (SQLException ex) {
            Logger.getLogger(ThanhToanTien.class.getName()).log(Level.SEVERE, null, ex);
        }
         String sql1 = ("exec Xuat_Tien '"+(String)JTableThue.getValueAt(row, 0)+ "'");
          try {
            ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql1);
            dfSDDV.setRowCount(0);
            while (ks.resultset.next()) {
                txtThanhTien.setText(df.format(ks.resultset.getDouble("ThanhTien")));     
                
            }

        } catch (SQLException ex) {
            Logger.getLogger(ThanhToanTien.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }//GEN-LAST:event_JTableThueMouseClicked

