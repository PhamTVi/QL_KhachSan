    private void pnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pnLuuActionPerformed
        // TODO add your handling code here:
        if (txtMaDV.getText().isEmpty() || txtMaThue.getText().isEmpty() || txtNgaySD.getText().isEmpty() || txtMaSD.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập ngày chọn phòng!!");
        } else {
          
            if (KiemtraMaSoTonTai(txtMaDV.getText())) {
                JOptionPane.showMessageDialog(null, "Đã trung Mã SD");
            } else {
                try {
                    String sqlInsert = "Insert into SDDichVu "
                            + "Values('" + txtMaSD.getText() + "', N'" + txtMaThue.getText() + "' , N'" + txtMaDV.getText() + "', '" + txtNgaySD.getText() + "', '" + txtDonGia.getText() + "' )";
                    String cn_DonGia = "exec CN_DONGIA_SDDV";
                    int x = ks.statement.executeUpdate(sqlInsert);
                    int y = ks.statement.executeUpdate(cn_DonGia);
                    if (x >= 1  || y >= 1) {
                        HienThiCSDLSDDV();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(SDDichVU.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_pnLuuActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(txtNgaySD.getText().isEmpty()){
         JOptionPane.showMessageDialog(null, "Vui lòng nhập ngày!!");
        }
        String sql = ("exec Xem_ThuePhong_VaoNgay  '"+ txtNgaySD.getText()+"'");
        try {
            ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql);
            dfThue.setRowCount(0);
            while (ks.resultset.next()) {
                String Mathue = ks.resultset.getString("MaThue");
                String MaP = ks.resultset.getString("MaP");
                Date NgayVao = ks.resultset.getDate("NgayVao");
                String MaKH = ks.resultset.getString("MaKH");
                String MaNV = ks.resultset.getString("MaNV");
                Double DatCoc = ks.resultset.getDouble("DatCoc");
                Date NgayRa = ks.resultset.getDate("NgayRa");
                Vector<Object> vec = new Vector<Object>();
                vec.add(Mathue);
                vec.add(MaP);
                vec.add(NgayVao);
                vec.add(MaKH);
                vec.add(MaNV);
                vec.add(DatCoc);
                vec.add(NgayRa);
                dfThue.addRow(vec);
            }

        } catch (SQLException ex) {
            Logger.getLogger(SDDichVU.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

