    public boolean KiemtraMaSoTonTai(String ma) {
        try {
            String ql = "select * from SDDichVu where MaSD ='" + ma + "'";
            ResultSet rs = ks.statement.executeQuery(ql);
            if (rs.next()) {
                return true;
            } else {
                return false;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return false;
    }

    private void HienThiCSDLDV() {
        String sql = ("select *from DichVu");
        try {
            ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql);
            dfQLKH.setRowCount(0);
            while (ks.resultset.next()) {
                String ma = ks.resultset.getString("MaDV");
                String ten = ks.resultset.getString("TenDV");
                Double giaTien = ks.resultset.getDouble("GiaTien");
                Vector<Object> vec = new Vector<Object>();
                vec.add(ma);
                vec.add(ten);
                vec.add(giaTien);
                dfQLKH.addRow(vec);
            }

        } catch (SQLException ex) {
            Logger.getLogger(SDDichVU.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void HienThiCSDLSDDV() {
        String sql = ("select *from SDDICHVU");
        try {
            ks.statement = ks.connection.createStatement();
            ks.resultset = ks.statement.executeQuery(sql);
            dfSDDV.setRowCount(0);
            while (ks.resultset.next()) {
                String maSD = ks.resultset.getString("MaSD");
                String maThue = ks.resultset.getString("MaThue");
                String maDV = ks.resultset.getString("MaDV");
                Date ngaySD = ks.resultset.getDate("NgaySD");
                Double giaTien = ks.resultset.getDouble("DonGia");

                Vector<Object> vec = new Vector<Object>();
                vec.add(maSD);
                vec.add(maThue);
                vec.add(maDV);
                vec.add(ngaySD);
                vec.add(giaTien);
                dfSDDV.addRow(vec);
            }

        } catch (SQLException ex) {
            Logger.getLogger(SDDichVU.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

